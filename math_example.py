import math

# Finding the square root of a number
square_root = math.sqrt(16)
print(f"Square Root of 16: {square_root}")

# Calculating the factorial of a number
factorial = math.factorial(5)
print(f"Factorial of 5: {factorial}")

# Using trigonometric functions (cosine of 45 degrees)
radians = math.radians(45)  # Convert degrees to radians
cosine_value = math.cos(radians)
print(f"Cosine of 45 degrees: {cosine_value}")

# Logarithms (natural logarithm of 10)
log_value = math.log(10)
print(f"Natural Log of 10: {log_value}")

# Constants: Pi and Euler's number
print(f"Value of Pi: {math.pi}")
print(f"Value of Euler's number (e): {math.e}")
